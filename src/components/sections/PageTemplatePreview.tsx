function parseCardItem(value: string) {
  const { cleanValue, size } = extractCardSize(value);
  const [title, ...bodyParts] = cleanValue.split(/\s+[—-]\s+/);
  const body = bodyParts.join(" - ").trim();

  return {
    body,
    cardSize: size,
    imageLabel: title.trim(),
    size,
    title: title.trim(),
  };
}

function getCardSize(value: string | undefined) {
  if (!value) {
    return undefined;
  }

  const normalizedValue = value.toLowerCase().trim();

  if (
    normalizedValue === "true" ||
    normalizedValue === "featured" ||
    normalizedValue === "large" ||
    normalizedValue === "larger" ||
    normalizedValue.includes("large") ||
    normalizedValue.includes("featured") ||
    normalizedValue.includes("3 col") ||
    normalizedValue.includes("3-col") ||
    normalizedValue.includes("three col")
  ) {
    return "large" as const;
  }

  if (normalizedValue === "medium") {
    return "medium" as const;
  }

  if (normalizedValue === "small") {
    return "small" as const;
  }

  return undefined;
}

function getGalleryImageSize(value: string | undefined) {
  if (!value) {
    return undefined;
  }

  const normalizedValue = value.toLowerCase().trim();

  switch (normalizedValue) {
    case "small":
      return "small" as const;
    case "medium":
      return "medium" as const;
    case "large":
      return "large" as const;
    case "tall":
      return "tall" as const;
    case "wide":
      return "wide" as const;
    default:
      return undefined;
  }
}

function extractCardSize(value: string) {
  const sizePattern = /\[(large|larger|featured|medium|small|3 col|3-col|three col)\]/i;
  const match = value.match(sizePattern);
  const size = getCardSize(match?.[1]);

  return {
    cleanValue: value.replace(sizePattern, "").trim(),
    size,
  };
}

function parsePageLink(value: string) {
  const [content, href = "#"] = value.split(/\s*->\s*/);
  const [labelAndTitle, ...bodyParts] = content.split(/\s+[—-]\s+/);
  const [label, title] = labelAndTitle.split(/\s*:\s*/);

  return {
    body: bodyParts.join(" - ").trim(),
    href: href.trim() || "#",
    label: label?.trim() || "Page",
    title: title?.trim() || label?.trim() || "Learn more",
  };
}

function parseFaqItem(value: string) {
  const [question, ...answerParts] = value.split(/\s+[—-]\s+/);

  return {
    answer: answerParts.join(" - ").trim(),
    question: question.trim(),
  };
}

function parseTestimonialItem(value: string) {
  const parts = value
    .split(/\s+[—-]\s+/)
    .map((part) => part.trim())
    .filter(Boolean);

  if (parts.length < 2) {
    return {
      author: "Customer",
      city: "",
      detail: "",
      quote: value.trim(),
      service: "",
    };
  }

  const [firstPart, ...restParts] = parts;
  const secondPart = restParts.join(" - ");
  const attributionFirst = isLikelyTestimonialAttribution(
    firstPart,
    secondPart,
  );
  const quote = attributionFirst ? secondPart : firstPart;
  const attribution = attributionFirst ? firstPart : secondPart;
  const parsedAttribution = parseTestimonialAttribution(attribution);

  return {
    author: parsedAttribution.author,
    city: parsedAttribution.detail,
    detail: parsedAttribution.detail,
    quote,
    service: parsedAttribution.detail,
  };
}

function parseTestimonialAttribution(value: string) {
  const [author, ...detailParts] = value.split(/\s*,\s*/);
  const detail = detailParts.join(", ").trim();

  return {
    author: author.trim() || "Customer",
    detail,
  };
}

function isLikelyTestimonialAttribution(candidate: string, quote: string) {
  const compactCandidate = candidate.trim();
  const compactQuote = quote.trim();

  if (!compactCandidate || !compactQuote) {
    return false;
  }

  const hasNameInitial = /\b[A-Z][a-z]+(?:\s+[A-Z]\.)/.test(compactCandidate);
  const hasLocationDetail = /^[^,]{2,40},\s*[^,]{2,40}$/.test(
    compactCandidate,
  );
  const isMuchShorter =
    compactCandidate.length <= 60 &&
    compactQuote.length > compactCandidate.length + 20;

  return (hasNameInitial || hasLocationDetail) && isMuchShorter;
}

function parseLink(value: string) {
  const [labelAndItems, href] = value.split(/\s*->\s*/);
  const [label, itemsText] = labelAndItems.split(/\s*:\s*/);

  return {
    href: href?.trim() || "#",
    items: itemsText ? splitItems(itemsText) : undefined,
    label: label?.trim() || value,
  };
}

function mergeNavLinkDropdown(
  link: ReturnType<typeof parseLink> | SiteNavigationLink,
) {
  const fallbackLink = sectionLibraryV3Content.navPrimary.links.find(
    (item) => normalizeLabel(item.label) === normalizeLabel(link.label),
  );

  return {
    ...link,
    items: "items" in link ? link.items : fallbackLink?.items,
  };
}

function normalizeLabel(value: string) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "")
    .replace(/s$/, "");
}

function splitItems(value: string) {
  const normalizedValue = value.trim();

  if (!normalizedValue) {
    return [];
  }

  const hasLineBreaks = /\r?\n/.test(normalizedValue);
  const separator = hasLineBreaks ? /\r?\n/ : /,|;/;

  return normalizedValue
    .split(separator)
    .map((item) => item.trim())
    .filter(Boolean);
}

function stripLabel(value: string) {
  return value.replace(/^[^:]+:\s*/, "").trim();
}
